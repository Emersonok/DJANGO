//For Loop (Print "Hello" 5 times)

word = "Hello"

for (i = 1; i <= word.length; i++){
    console.log(word)
}

//While Loop (Print "Hello 5 times")

i = 1
while (i <= word.length){
    console.log(word)
    i++
}

//For Loop (Odd numbers from 1 to 25)

for (i = 1 ; i < 26; i++){
    if (i%2 == 1){
        console.log(i)
    }
}


//While Loop (Odd numbers from 1 to 25)

i = 1

while (i < 26){
    if(i%2 != 0){
        console.log(i)
    }
    i++
}


